    private void fillTab() throws Exception {
        
        tbPacientes.setModel(dm);
        
        PreparedStatement ps = null;
        ResultSet rs = null;
        
        Conexion conn = new Conexion();
        Connection con  = conn.getConnection();
        
        String sqlQuery = "Select * From pacientes";
        
        
        ps = con.prepareStatement(sqlQuery);
        rs = ps.executeQuery();
        
        ResultSetMetaData rsMd = rs.getMetaData();
        int xd = rsMd.getColumnCount();
        
        dm.addColumn("ID");
        dm.addColumn("Nombre completo");
        dm.addColumn("Genero");
        dm.addColumn("Edad");
        dm.addColumn("Contacto");
        //dm.addColumn("");
        
        
        while(rs.next()){
            Object[] filas = new Object[xd];
            
            for(int i=0;i<xd;i++){
                filas[i] = rs.getObject(i+1);
            }
            
            dm.addRow(filas);
        }
    }
   
    private void filterTable(String query){
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(dm);
        tbPacientes.setRowSorter(trs);
        
        trs.setRowFilter(RowFilter.regexFilter(query));
    }
    

